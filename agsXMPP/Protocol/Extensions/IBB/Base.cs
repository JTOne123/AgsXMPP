using AgsXMPP.Xml.Dom;

namespace AgsXMPP.Protocol.Extensions.IBB
{
	/// <summary>
	/// IBB base class
	/// </summary>
	public abstract class Base : Element
	{
		public Base()
		{
			this.Namespace = URI.IBB;
		}

		/// <summary>
		/// Sid
		/// </summary>
		public string Sid
		{
			get { return this.GetAttribute("sid"); }
			set { this.SetAttribute("sid", value); }
		}
	}
}
